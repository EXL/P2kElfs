void* yeti_load_file(char* filename)
{
  void* r = 0;
  FILE* fp = fopen(filename, "rb");

  if (fp)
  {
    if (!fseek(fp, 0, SEEK_END))
    {
      int n = ftell(fp);
      if ((r = malloc(n)) != 0)
      {
        fseek(fp, 0, SEEK_SET);
        fread(r, 1, n, fp);
      }
    }
    fclose(fp);
  }
  return r;
}
